package com.sky.pojo;

import java.io.Serializable;
import java.util.Date;

public class ColumnDescription implements Serializable {
	
	
	private String tableName;
	private String columnName;
	private String colDescription;
	private String dataType;
	private String dataLenght;
	private String creator;
	private Date createDate;
	public String getTableName() {
		return tableName;
	}
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	public String getColumnName() {
		return columnName;
	}
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}
	
	public String getDataType() {
		return dataType;
	}
	public void setDataType(String dataType) {
		this.dataType = dataType;
	}
	public String getCreator() {
		return creator;
	}
	public void setCreator(String creator) {
		this.creator = creator;
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	public String getColDescription() {
		return colDescription;
	}
	public void setColDescription(String colDescription) {
		this.colDescription = colDescription;
	}
	public String getDataLenght() {
		return dataLenght;
	}
	public void setDataLenght(String dataLenght) {
		this.dataLenght = dataLenght;
	}
	@Override
	public String toString() {
		return "ColumnDescription [tableName=" + tableName + ", columnName="
				+ columnName + ", colDescription=" + colDescription
				+ ", dataType=" + dataType + ", dataLenght=" + dataLenght
				+ ", creator=" + creator + ", createDate=" + createDate + "] \n";
	}
	
	
	

	
	
	

}
