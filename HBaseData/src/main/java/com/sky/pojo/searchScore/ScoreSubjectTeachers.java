package com.sky.pojo.searchScore;

public class ScoreSubjectTeachers {
	
	private String subject_id;
	private String subject_name;
	private String examType_id;
	private String area_id;
	private String exam_date;
	private String login_id;
	private String login_name;
	private String item_id;
	public String getSubject_id() {
		return subject_id;
	}
	public void setSubject_id(String subject_id) {
		this.subject_id = subject_id;
	}
	public String getSubject_name() {
		return subject_name;
	}
	public void setSubject_name(String subject_name) {
		this.subject_name = subject_name;
	}
	public String getExamType_id() {
		return examType_id;
	}
	public void setExamType_id(String examType_id) {
		this.examType_id = examType_id;
	}
	public String getArea_id() {
		return area_id;
	}
	public void setArea_id(String area_id) {
		this.area_id = area_id;
	}
	public String getExam_date() {
		return exam_date;
	}
	public void setExam_date(String exam_date) {
		this.exam_date = exam_date;
	}
	public String getLogin_id() {
		return login_id;
	}
	public void setLogin_id(String login_id) {
		this.login_id = login_id;
	}
	public String getLogin_name() {
		return login_name;
	}
	public void setLogin_name(String login_name) {
		this.login_name = login_name;
	}
	public String getItem_id() {
		return item_id;
	}
	public void setItem_id(String item_id) {
		this.item_id = item_id;
	}
	@Override
	public String toString() {
		return "ScoreSubjectTeachers [subject_id=" + subject_id
				+ ", subject_name=" + subject_name + ", examType_id="
				+ examType_id + ", area_id=" + area_id + ", exam_date="
				+ exam_date + ", login_id=" + login_id + ", login_name="
				+ login_name + ", item_id=" + item_id + "] \n";
	}
	

}
